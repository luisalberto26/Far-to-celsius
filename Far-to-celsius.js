// Este programa convierte grados Celsius a Fahrenheit.
//Fórmula: F = (C × 9/5) + 32
// Se declara la variable celsius como número y se calcula fahrenheit.
// Finalmente se muestran los resultados en consola.

let celsius = 30; // Number
let fahrenheit = (celsius * 9/5) + 32; // Number

console.log("Celsius: " + celsius);
console.log("Resultado: " + fahrenheit + "°F");

